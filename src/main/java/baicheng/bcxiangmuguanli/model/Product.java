package baicheng.bcxiangmuguanli.model;

public class Product {
    private String product_id;
    private String product_name;
    private String product_boss;
    private String product_start_time;
    private String product_end_time;
    private String product_explain;

    public String getProduct_id() {
        return product_id;
    }

    public void setProduct_id(String product_id) {
        this.product_id = product_id;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public String getProduct_boss() {
        return product_boss;
    }

    public void setProduct_boss(String product_boss) {
        this.product_boss = product_boss;
    }

    public String getProduct_start_time() {
        return product_start_time;
    }

    public void setProduct_start_time(String product_start_time) {
        this.product_start_time = product_start_time;
    }

    public String getProduct_end_time() {
        return product_end_time;
    }

    public void setProduct_end_time(String product_end_time) {
        this.product_end_time = product_end_time;
    }

    public String getProduct_explain() {
        return product_explain;
    }

    public void setProduct_explain(String product_explain) {
        this.product_explain = product_explain;
    }
}
